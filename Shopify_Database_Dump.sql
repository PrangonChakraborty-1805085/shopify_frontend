CREATE OR REPLACE
PROCEDURE "PRANGON"."CHECKOUT" AS
BEGIN
order_id_var := order_id_seq.currval;
select distinct seller_id into seller_id_var from products where stock_id = stock_id_var;

--total price of products
select distinct price into price_var from products where STOCK_ID = stock_id_var;
total_price := price_var * quantity;
select offer into offer_var from products where stock_id = stock_id_var fetch first 1 rows only;
if offer_var is not null then
total_price := total_price - (total_price * offer_var / 100);
END if;

--deduct customer WALLET
select wallet into wallet_var from accounts WHERE email = (SELECT email from customers where customer_id = customer_id_var);
update ACCOUNTS
set wallet = (wallet_var - total_price)
where email = (SELECT email from customers where customer_id = customer_id_var);

--increase seller wallet
select wallet into wallet_seller_var from accounts WHERE email = (SELECT email from sellers where seller_id = seller_id_var);
update ACCOUNTS
set WALLET = (wallet_seller_var + total_price)
where email = (SELECT email from sellers where seller_id = seller_id_var);

--if order id and seller id is both presenet in transactions table then update the transaction amount and sysdate else inser a new row
select count(*) into counter from transactions where order_id = order_id_var and seller_id = seller_id_var;
if counter = 0 THEN
insert into transactions values (transaction_id_seq.nextval, order_id_var, customer_id_var, seller_id_var, total_price, sysdate);
else
select amount into amount_var from transactions where order_id = order_id_var and seller_id = seller_id_var;
update TRANSACTIONS
set amount = (amount_var + total_price)
where order_id = order_id_var and seller_id = seller_id_var;
end if;

for i in 1..quantity
LOOP
select product_id into product_id_var 
from products 
where stock_id = stock_id_var and order_id is null 
fetch first 1 rows only;
update products
set order_id = order_id_var
where product_id = product_id_var;
--here i have to commit
END LOOP;
END;
